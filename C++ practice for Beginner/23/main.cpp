#include <iostream>
#include <cmath>

using namespace std;

int main()
{
    cout<<sqrt(9)<<endl;
    cout<<abs(-16)<<endl;
    cout<<fabs(-16.89)<<endl;
    cout<<pow(-4, 2)<<endl;
    cout<<sin(90)<<endl;
    cout<<tolower('A')<<endl;
    cout<<toupper('z')<<endl;
    cout<<floor(9.97)<<endl;
    return 0;
}
